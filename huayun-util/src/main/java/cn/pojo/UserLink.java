package cn.pojo;


public class UserLink {

  private Integer lid;
  private String limage;
  private String lmoney;
  private String lsex;
  private String laddress;
  private String lphone;
  private Integer lcollect;


  public Integer getLid() {
    return lid;
  }

  public void setLid(Integer lid) {
    this.lid = lid;
  }


  public String getLimage() {
    return limage;
  }

  public void setLimage(String limage) {
    this.limage = limage;
  }


  public String getLmoney() {
    return lmoney;
  }

  public void setLmoney(String lmoney) {
    this.lmoney = lmoney;
  }


  public String getLsex() {
    return lsex;
  }

  public void setLsex(String lsex) {
    this.lsex = lsex;
  }


  public String getLaddress() {
    return laddress;
  }

  public void setLaddress(String laddress) {
    this.laddress = laddress;
  }


  public String getLphone() {
    return lphone;
  }

  public void setLphone(String lphone) {
    this.lphone = lphone;
  }


  public Integer getLcollect() {
    return lcollect;
  }

  public void setLcollect(Integer lcollect) {
    this.lcollect = lcollect;
  }

}
